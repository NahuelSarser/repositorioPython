numero1 = int(input("ingrese numero entero 1: "))
numero2 = int(input("ingrese numero entero 2: "))
operacion = input("Ingrese la operacion que desea realizar con el simbolo: ")
if operacion == "+":
    print(numero1+numero2)
elif operacion =="-":
    print(numero1-numero2)
elif operacion =="*":
    print(numero1*numero2)
elif operacion =="/":
    print(numero1/numero2)
elif operacion =="modulo":
    print(numero1%numero2)

